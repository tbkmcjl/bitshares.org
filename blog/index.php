           <p>
           As is always the case with new technology, the language often lacks words for the new concepts associated with the innovation, as we are forced to use existing words in strange new ways, as our ancestors did when faced with the ‘horseless carriage’ (automobile), ‘talking picture’ (movie), or ‘information superhighway’ (Internet). Thus, while it is true that words have meanings, it is equally true that a rose by any other name is still a rose.
